import KolbBotKit
import KolbBotProtocol
import Foundation

// Prefer the KolbBotKit wrapper to keep gateway request payloads consistent.
typealias AnyCodable = KolbBotKit.AnyCodable
typealias InstanceIdentity = KolbBotKit.InstanceIdentity

extension AnyCodable {
    var stringValue: String? { self.value as? String }
    var boolValue: Bool? { self.value as? Bool }
    var intValue: Int? { self.value as? Int }
    var doubleValue: Double? { self.value as? Double }
    var dictionaryValue: [String: AnyCodable]? { self.value as? [String: AnyCodable] }
    var arrayValue: [AnyCodable]? { self.value as? [AnyCodable] }

    var foundationValue: Any {
        switch self.value {
        case let dict as [String: AnyCodable]:
            dict.mapValues { $0.foundationValue }
        case let array as [AnyCodable]:
            array.map(\.foundationValue)
        default:
            self.value
        }
    }
}

extension KolbBotProtocol.AnyCodable {
    var stringValue: String? { self.value as? String }
    var boolValue: Bool? { self.value as? Bool }
    var intValue: Int? { self.value as? Int }
    var doubleValue: Double? { self.value as? Double }
    var dictionaryValue: [String: KolbBotProtocol.AnyCodable]? { self.value as? [String: KolbBotProtocol.AnyCodable] }
    var arrayValue: [KolbBotProtocol.AnyCodable]? { self.value as? [KolbBotProtocol.AnyCodable] }

    var foundationValue: Any {
        switch self.value {
        case let dict as [String: KolbBotProtocol.AnyCodable]:
            dict.mapValues { $0.foundationValue }
        case let array as [KolbBotProtocol.AnyCodable]:
            array.map(\.foundationValue)
        default:
            self.value
        }
    }
}
